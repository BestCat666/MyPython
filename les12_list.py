# МЕТОДЫ СПИСКОВ    #list_. + CTRL + ПРОБЕЛ = показывает все методы!!!!
list_=[1,2,3,4,3,5,6,6]
list_.append(46)  # добавление элемента в конец списка, только ОДИН аргумент
print(list_)
#list_.clear()   # очистка списка
#print(list_)
k = list_.copy()  # копирование спика, это ДВА ОТДЕЛЬНЫХ ОБЪЕКТА
print(k)
print(list_.count(6)) # сколько раз встретилось указанное значение
print(list_.index(1)) # ищет индекс указанного значения, первого найденного
print(list_.index(3,3)) # ищет индекс указанного значения, второй аргумент - с какого индекса начинать поиск
print(list_.index(3,3,5)) # значение, с какого индекса, по какой индекс НЕ ВКЛЮЧАЯ ЕГО
list_.insert(2,1000) # на какой индекс втавить значение
print(list_)
print(list_.pop()) # удаляет с конца значение и показывает его
list_.pop(0)  # удаляет значение по указанному индексу
print(list_)
list_.remove(4)  # удаляет указанное значение
print(list_)

list_.reverse()  #переворачивает список
print(list_)

list_.sort()  # сортировка по возрастанию
print(list_)

list_.sort(reverse = True) # сортировка по убыванию
print(list_)

