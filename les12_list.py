#ИНДЕКСЫ И СРЕЗЫ

b = [1,2,56,34,23,12]
print(b[0])   # первое место
print(b[-1])  # последнее место
print(b[1:4]) # c 1 по 4 индекс НЕ ВКЛЮЧАЯ ЕГО
print(b[0:])  # c 0 индекса по последний
print(b[:2])  # c начала по 2 индекс НЕ ВКЛЮЧАЯ ЕГО
print(b[:])  # весь список
print(b[::2])  # весь список с шагом 2
print(b[2:5:2])  # весь список с шагом 2 от 2 до 5 индекса НЕ ВКЛЮЧАЯ ЕГО
print(b[::-1])  # перевернуть список
b[0] = 1000  #изменение 1 элемента списка 
print(b)
b[2:5] = 5000, 6000, 7000
print(b)
del b[0]
print(b)

